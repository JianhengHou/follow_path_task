import random


# Prompts for the follow-path task.
# The primary prompt requested by the user (Chinese):
# '预测对象跟随指定路径运动'
PROMPTS = {
    "default": [
        "Animate a geometric object following the drawn trajectory until its center ends exactly at the arrow tip.",
    ]
}


def get_prompt(task_type: str = "default") -> str:
    """
    Select a random prompt for the given task type.

    Args:
        task_type: Type of task (key in PROMPTS dict)

    Returns:
        Random prompt string from the specified type
    """
    prompts = PROMPTS.get(task_type, PROMPTS["default"])
    return random.choice(prompts)


def get_all_prompts(task_type: str = "default") -> list[str]:
    """Get all prompts for a given task type."""
    return PROMPTS.get(task_type, PROMPTS["default"])
